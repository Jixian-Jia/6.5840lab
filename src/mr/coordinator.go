type Coordinator struct {
	// Your definitions here.
	mu          sync.Mutex
	phase       Phase
	mapTasks    []TaskInfo
	reduceTasks []TaskInfo
	nReduce     int
	imFiles     [][]string // store the names of intermediate files generated by the mappers
	finalFiles  []string
}

type TaskInfo struct {
	files     []string // in case of reduce tasks, there are multiple files
	state     TaskState
	startTime time.Time
}
type Phase int

const (
	MapPhase Phase = iota
	ReducePhase
	Finish
)

type TaskState int

const (
	Scheduled TaskState = iota
	InProcess
	Done
)

// Your code here -- RPC handlers for the worker to call.

func (c *Coordinator) AllocateWork(args *RequestTaskArgs, reply *RequestTaskReply) error {

	c.mu.Lock()
	defer c.mu.Unlock()
	// allocate mapping or reducing work, depending on the phase
	// ask the worker to stop if all work are finished
	// put the worker on wait, if too much worker

	if c.phase == Finish {
		reply.TaskType = ExitTask
		return nil
	} else if c.phase == MapPhase {
		// find the first task to be scheduled
		var nextTask *TaskInfo
		var taskID int
		for i := range c.mapTasks {
			task := &c.mapTasks[i]
			if task.state == Scheduled {
				nextTask = task
				taskID = i
				break
			}
		}
		// if all works are Inprocess, tell the new worker to wait
		if nextTask == nil {
			reply.TaskType = WaitTask
		} else {
			reply.TaskType = MapTask
			reply.FileNames = nextTask.files
			reply.NReduce = c.nReduce
			reply.TaskId = taskID

			nextTask.startTime = time.Now()
			nextTask.state = InProcess
		}
	} else {
		//c.phase == ReducePhase
		// assume []reduceTask has been initialized somewhere else
		var nextTask *TaskInfo
		var taskID int
		for i := range c.reduceTasks {
			task := &c.reduceTasks[i]
			if task.state == Scheduled {
				nextTask = task
				taskID = i
				break
			}
		}
		// if all works are Inprocess, tell the new worker to wait
		if nextTask == nil {
			reply.TaskType = WaitTask
		} else {
			reply.TaskType = ReduceTask
			reply.FileNames = nextTask.files
			reply.NReduce = c.nReduce
			reply.TaskId = taskID

			nextTask.startTime = time.Now()
			nextTask.state = InProcess
		}
	}

	return nil
}

func (c *Coordinator) UpdateWork(args *UpdateArgs, reply *UpdateReply) error {

	c.mu.Lock()
	defer c.mu.Unlock()

	if c.phase == MapPhase && args.TaskType != MapTask {
		return fmt.Errorf("received reduce update during map phase")
	}

	if c.phase == ReducePhase && args.TaskType != ReduceTask {
		return fmt.Errorf("received map update during reduce phase")
	}
	id := args.TaskId
	if args.TaskType == MapTask {
		if c.mapTasks[id].state != InProcess {
			return nil
		}
		c.mapTasks[id].state = Done
		c.imFiles[id] = args.FileNames
		return nil
	} else if args.TaskType == ReduceTask {
		if c.reduceTasks[id].state != InProcess {
			return nil
		}
		c.reduceTasks[id].state = Done
		c.finalFiles[id] = args.FileNames[0]
		return nil
	} else {
		return fmt.Errorf("error when updating the works")
	}

}


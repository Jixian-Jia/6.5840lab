// start a thread that listens for RPCs from worker.go
func (c *Coordinator) server(sockname string) {
	rpc.Register(c)
	rpc.HandleHTTP()
	os.Remove(sockname)
	l, e := net.Listen("unix", sockname)
	if e != nil {
		log.Fatalf("listen error %s: %v", sockname, e)
	}
	go http.Serve(l, nil)
}

// main/mrcoordinator.go calls Done() periodically to find out
// if the entire job has finished.
func (c *Coordinator) Done() bool {
	ret := false

	// Your code here.
	c.mu.Lock()
	defer c.mu.Unlock()
	if c.phase == Finish {
		ret = true
	}
	return ret
}

// create a Coordinator.
// main/mrcoordinator.go calls this function.
// nReduce is the number of reduce tasks to use.
func MakeCoordinator(sockname string, files []string, nReduce int) *Coordinator {
	c := Coordinator{}

	// Your code here.
	c.nReduce = nReduce
	c.phase = MapPhase

	// initialize map tasks
	for _, f := range files {
		c.mapTasks = append(c.mapTasks, TaskInfo{files: []string{f}, state: Scheduled, startTime: time.Time{}})
	}

	for i := 0; i < nReduce; i++ {
		// update this later
		c.reduceTasks = append(c.reduceTasks, TaskInfo{files: []string{}, state: Scheduled, startTime: time.Time{}})
	}

	c.imFiles = make([][]string, len(files))
	c.finalFiles = make([]string, nReduce)
	// todo: the coor should periodically check on dead workers
	// the dead workers should also restart themselves

	go func() {

		ticker := time.NewTicker(500 * time.Millisecond)
		defer ticker.Stop()

		for {
			<-ticker.C

			c.mu.Lock()

			if c.phase == Finish {
				c.mu.Unlock()
				return
			}

			c.checkTimeouts()
			c.checkCompletion()

			c.mu.Unlock()
		}
	}()
	c.server(sockname)
	return &c
}

func (c *Coordinator) checkTimeouts() {
	if c.phase == MapPhase {
		for i := range c.mapTasks {
			task := &c.mapTasks[i]
			if task.state == InProcess && time.Since(task.startTime) > 10*time.Second {
				task.state = Scheduled
			}
		}
	}
	if c.phase == ReducePhase {
		for i := range c.reduceTasks {
			task := &c.reduceTasks[i]
			if task.state == InProcess && time.Since(task.startTime) > 10*time.Second {
				task.state = Scheduled
			}
		}
	}
}

// check if the map phase has completed.
// if so, transition to the reduce phase and initialize  []reduceTaks
// also check if the reduce phase has completed, if so change the phase to finish
func (c *Coordinator) checkCompletion() {

	if c.phase == MapPhase {
		cmplt := true
		for _, task := range c.mapTasks {
			cmplt = cmplt && (task.state == Done)
		}
		if cmplt == true {
			// fill []reducetasks files from imfiles

			var bucketID, taskID int
			for _, fs := range c.imFiles {
				for _, f := range fs {
					_, err := fmt.Sscanf(f, "mr-%d-%d", &taskID, &bucketID)
					if err != nil {
						panic(err)
					}
					c.reduceTasks[bucketID].files = append(c.reduceTasks[bucketID].files, f)
				}
			}
			c.phase = ReducePhase
		}
		return
	}
	if c.phase == ReducePhase {
		cmplt := true
		for _, task := range c.reduceTasks {
			cmplt = cmplt && (task.state == Done)
		}
		if cmplt == true {
			c.phase = Finish
		}
		return
	}
}
